<h1 align="center"><b>BLOOD GROUPS</b></h1>
<p>There are four main blood groups (types of blood) – A, B, AB and O.<br>
 Your blood group is determined by the genes you<br> 
inherit from your parents.<br><br><br>

Each group can be either RhD positive or RhD negative,<br>
which means in total there are eight main blood groups.
</p>

<h2><b>Antibodies and antigens</b></h2>

	<p> Blood is made up of red blood cells, white blood cells and platelets in a liquid called plasma.<br> Your blood group is identified by antibodies and antigens in the blood.<br><br>

	Antibodies are proteins found in plasma.<br> They're part of your body's natural defences. They recognise foreign substances,<br> such as germs, and alert your immune system, which destroys them.<br><br>

	Antigens are protein molecules found on the surface of red blood cells. </p><br><br>
	<h1><b>The ABO system</b></h1>
		

	<p> <ol>
		<li><b>blood group A –</b>has A antigens on the red blood cells with anti-B antibodies in the plasma</li><br>
		<li><b>blood group B  – </b>has B antigens with anti-A antibodies in the plasma</li><br>
		<li><b>blood group O  – </b>has no antigens, but both anti-A and anti-B antibodies in the plasma</li><br>
		<li><b>has both A and B antigens,</b> but no antibodies</li><br>
	</ol></p>
	<br>
	<p>Blood group O is the most common blood group.<br> Almost half of the UK population (48%) has blood group O.<br> <br>

Receiving blood from the wrong ABO group can be life threatening. <br>For example, if someone with group B blood is given <br>group A blood, their anti-A antibodies will attack the group A cells.<br><br>

This is why group A blood must never be given to someone<br> who has group B blood and vice versa.

As group O red blood cells<br> <br>don't have any A or B antigens, it can safely be given to any other group.

The NHS Blood and Transplant <br>(NHSBT) website has more information about the<br> different blood groups.</p>
<br>


<h1><b>Blood group test</b></h1>
<p>To work out your blood group, your red cells are mixed with different antibody solutions. If, for example, the solution contains anti-B<br> antibodies and you have B antigens on your cells (you're blood group B), it will clump together.<br><br><br>

If the blood doesn't react to any of the anti-A or anti-B antibodies, it's blood group O. A series of tests with different types of <br>antibody can be used to identify your blood group.<br><br>

If you have a blood transfusion – where blood is taken from one person and given to another – your blood will be tested against a sample of<br> donor cells that contain ABO and RhD antigens. If there's no reaction, donor blood with the same ABO and RhD type can be used.<br></p>

